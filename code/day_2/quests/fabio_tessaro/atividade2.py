

idade = []
altura = []

i = 1


while i <= 5:
    age = input("Informe sua idade: ")
    idade.append(age)
    height = input("Informe sua altura: ")
    altura.append(height)
    i += 1

idade.reverse()
altura.reverse()
print("Idade em ordem inversa:")
print(idade)
print("Altura em ordem inversa:")
print(altura)







